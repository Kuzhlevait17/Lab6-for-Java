package ru.kuzhleva.lab6.annotations.BasicAnnotations;

/**
 * Перечисление, описывающее формирование строкового представления объекта.
 */
public enum Mode {
    /**
     * Объект, который будет сформирован в строковом представлении.
     */
    YES,
    /**
     *  Объект, который не будет сформирован в строковом представлении.
     */
    NO
}
